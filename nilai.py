nilai = float( input("masukan nilai anda "))

if nilai >= 10:
    print(" nilai kamu S ")
elif nilai >= 9:
    print (" nilai kamu A ")
elif nilai >= 7:
    print (" nilai kamu B ")
elif nilai >= 5:
    print (" nilai kamu D-")
else:
    print("makanya belajar yg bener biar nilainya bagus ")